    public function getSalaryStructureList(Request $request)
    {
        $companyId = $request->get('company_id');
        $type = $request->get('type');
        $isActive = $request->get('is_active');

        return $this->getListJson($companyId, $type, $isActive,
            [$this->_salaryStructureServiceInterface, 'salaryStructureList'],
            function (Collection $entities) {
                $rowJsonData = new Collection();

                foreach ($entities as $entity) {
                    $rowJsonData->push([
                        'id' => $entity->id,
                        'company' => Common::isDataExist($entity->company) ? $this->getCompanyObject($entity->company) : null,
                        'type' => $entity->type,
                        'name' => $entity->name,
                        'slug' => $entity->slug,
                        'description' => $entity->description,
                        'is_active' => $entity->is_active,
                        'created_by' => $entity->created_by,
                        'modified_by' => $entity->modified_by
                    ]);
                }

                return $rowJsonData;
            });
    }

