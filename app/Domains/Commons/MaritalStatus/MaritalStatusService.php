<?php

namespace App\Domains\Commons\MaritalStatus;

use App\Core\Domains\Contracts\Pagination\ListedSearchParameter;
use App\Core\Domains\Contracts\Pagination\PagedSearchParameter;
use App\Core\Services\Request\ListSearchRequest;
use App\Core\Services\Request\PageSearchRequest;
use App\Core\Services\Response\BasicResponse;
use App\Core\Services\Response\GenericCollectionResponse;
use App\Core\Services\Response\GenericListSearchResponse;
use App\Core\Services\Response\ObjectResponse;
use App\Core\Services\Response\GenericPageSearchResponse;
use App\Domains\ServiceAbstract;
use App\Domains\Commons\MaritalStatus\Contracts\MaritalStatusRepositoryInterface;
use App\Domains\Commons\MaritalStatus\Contracts\MaritalStatusServiceInterface;
use App\Domains\Commons\MaritalStatus\Contracts\MaritalStatusInterface;
use Cviebrock\EloquentSluggable\Services\SlugService;
use ErrorException;
use Exception;
use Illuminate\Support\Facades\Validator;

/**
 * MaritalStatusService Class
 * It has all useful methods for business logic.
 */
class MaritalStatusService extends ServiceAbstract implements MaritalStatusServiceInterface
{
    //<editor-fold desc="#field">

    /**
     * @var MaritalStatusRepositoryInterface
     */
    protected $repository;

    //</editor-fold>


    //<editor-fold desc="#constructor">

    /**
     * Loads our $repo with the actual Repo associated with our MaritalStatusInterface
     * MaritalStatusService constructor.
     *
     * @param MaritalStatusRepositoryInterface $repository
     */
    public function __construct(MaritalStatusRepositoryInterface $repository)
    {
        $this->repository = $repository;
    }

    //</editor-fold>


    //<editor-fold desc="#public (method)">
    
    /**
     * {@inheritdoc}
     */
    public function create(MaritalStatusInterface $MaritalStatus): ObjectResponse
    {
        $response = new ObjectResponse();

        $validator = Validator::make($MaritalStatus->toArray(), [
            'name' => 'required',
            'slug' => 'required'
        ]);

        if ($validator->fails()) {
            $response->addErrorMessageResponse($response->getMessageCollection(), $validator->errors(), 400);

            return $response;
        }

        $this->setAuditableInformationFromRequest($MaritalStatus);

        try {
            $result = $this->repository->create($MaritalStatus);

            $response->setResult($result);
            $response->addSuccessMessageResponse($response->getMessageCollection(), 'Marital status was created', 200);
        } catch (Exception $ex) {
            if (method_exists($ex,'getResponse')) {
                $exception = new ErrorException((string) $ex->getResponse()->getBody());
                $response->addErrorMessageResponse($response->getMessageCollection(), json_decode($exception->getMessage(), true)['message'], $ex->getCode());
            }

            $response->addErrorMessageResponse($response->getMessageCollection(), (string) $ex->getMessage(), 500);
        }

        return $response;
    }

    /**
     * {@inheritdoc}
     */
    public function update(MaritalStatusInterface $MaritalStatus): BasicResponse
    {
        $response = new ObjectResponse();

        $validator = Validator::make($MaritalStatus->toArray(), [
            'name' => 'required',
